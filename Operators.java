// Types Of Operators in JAVA 
public  class Operators{
    public static void main(String[]args){
        Arithmetic_Operators();
    }
// Assignment Operators In Java
public static void Arithmetic_Operators(){
    int a = 20;
    int b = 10;

    System.out.println(a+b);
    System.out.println(a-b);
    System.out.println(a*b);
    System.out.println(a/b);
    System.out.println(a%b);
}

}